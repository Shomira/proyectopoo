    public void visualizar_productos_en_la_lista(){
        // creacion de la lista prodNombrs para visualizar luego 
        String [] prodN  = new String[product.size()];
        
        // cadena para concatenar y mostrar espacio
        String cadena = "----------------------";
        
        int a = 0;
        // recorrer los productos en la lista
        for(Producto producto: product){
            prodN[a] = producto.getNombre() + cadena + String.valueOf
           (producto.getCantidad());
           // sumar contador
            a++;
     
        }
        // accede al nombre del cajon de la lista para presentar el nombre 
        lstproductos.setListData(prodN);
      
    
   }
