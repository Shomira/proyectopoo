    private void bntguardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntguardar1ActionPerformed
        // TODO add your handling code here:
        
        // Vamos a obtener el valor de las cajas de texto
        String nombre =  txtnombre.getText();
        String unidad = txtunidad.getText();
        int cantidad = Integer.parseInt(txtcantidad.getText());
        double precio = Double.parseDouble(txtprecio.getText());

        // Creamos un objeto tipo producto enviandole los parametros
        Producto producto = new Producto(nombre, unidad, cantidad, precio );

        // Agregamos el producto a nuestra lista de objetos llamada product
        product.add(producto);
        
        // metodo que visualiza la lista de productos 
        visualizar_productos_en_la_lista();
        
        // llamada al metodo para vaciar las cajas de texto
        limpiar();

        
    }//GEN-LAST:event_bntguardar1ActionPerformed

